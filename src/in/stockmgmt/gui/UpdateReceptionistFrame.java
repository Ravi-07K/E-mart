    private void loadReceptionistId() {
        try{
            receptionist=ReceptionistDAO.getAllReceptionistId();
            if(receptionist.isEmpty()){
               JOptionPane.showMessageDialog(null, "No receptionist present"); 
               btnUpdate.setEnabled(false);
               return;
            }
            btnUpdate.setEnabled(true);
            cmbRcpId.removeAllItems();
            Set<String> keys=receptionist.keySet();
            for(String x:keys){
                cmbRcpId.addItem(x);
            }
        }
        catch(SQLException ex){
         JOptionPane.showMessageDialog(null, "DB Eror", "Error", JOptionPane.ERROR_MESSAGE);
         ex.printStackTrace();
        }
    }

   private boolean validateInputs() {
        return !(pwdPassword.getPassword().length<4 || pwdCnfPassword.getText().length()<4);
    }

    private boolean validatePwdMatch(String a,String b) {
        return (a.equals(b));
    }

    private void clearText() {
        pwdPassword.setText("");
        pwdCnfPassword.setText("");
    }

    
