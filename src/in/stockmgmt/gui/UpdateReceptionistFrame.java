    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
       if(!validateInputs()){
            JOptionPane.showMessageDialog(null, "Password must be atleast 4 characters long", "Incomplete Data!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String pwd=String.valueOf(pwdPassword.getPassword());
        String repwd=pwdCnfPassword.getText();
        if(!validatePwdMatch(pwd,repwd)){
            JOptionPane.showMessageDialog(null, "Password doesn't match", "Incomplete Data!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
        UserPojo user=ReceptionistDAO.getUserById(cmbRcpId.getSelectedItem().toString());;
        user.setPassword(pwdCnfPassword.getText());
        user.setUsername(txtRcpName.getText());
        
        boolean result=ReceptionistDAO.updateReceptionist(cmbRcpId.getSelectedItem().toString(),pwd);
        if(!result){
            JOptionPane.showMessageDialog(null, "Record Not updated", "Error", JOptionPane.ERROR_MESSAGE);
            clearText();
            return;
        }
        JOptionPane.showMessageDialog(null, "Record Updated Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
        clearText();
        cmbRcpId.setSelectedIndex(0);
        }
        catch(SQLException ex){
         JOptionPane.showMessageDialog(null, "DB Eror", "Error", JOptionPane.ERROR_MESSAGE);
         ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

